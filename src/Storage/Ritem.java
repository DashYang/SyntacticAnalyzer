package Storage;

public class Ritem {
	private int[] states;
	private String sympol;
	private String go2;
	public int[] getStates() {
		return states;
	}
	public void setStates(int[] states) {
		this.states = states;
	}
	public String getSympol() {
		return sympol;
	}
	public void setSympol(String sympol) {
		this.sympol = sympol;
	}
	public String getGo2() {
		return go2;
	}
	public void setGo2(String go2) {
		this.go2 = go2;
	}
	
	
}
